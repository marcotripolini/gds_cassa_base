package it.gds.point.beans;

public class TavoloStatus {
	private String id;
	private String nome_tavolo;
	private int occupato;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNome_tavolo() {
		return nome_tavolo;
	}

	public void setNome_tavolo(String nome_tavolo) {
		this.nome_tavolo = nome_tavolo;
	}

	public int getOccupato() {
		return occupato;
	}

	public void setOccupato(int occupato) {
		this.occupato = occupato;
	}

}
